public class Funcionario {
    private int codigo;
    private String nome;
    private int CPF;
    private String endereco;
    private int telefone;
    private int idade;
    private float salario;

    public Funcionario(int codigo, String nome, int CPF, String endereco, int telefone, int idade, float salario){
        this.codigo = codigo;
        this.nome = nome;
        this.CPF = CPF;
        this.endereco = endereco;
        this.telefone = telefone;
        this.idade = idade;
        this.salario = salario;
    }

    

}
